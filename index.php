    <section>
      <p>When people say "classical music," they usually mean anything you would hear an Eastman student play. But Western music of itself had a classial period, with its own distinctions, complexities and herioc performers.</p> 

      <p>The dates of the classical period in Western music are generally accepted as being between 1750 and 1820. It falls right between the Baroque and Romantic periods. </p>  

      <p>During the era, music moved away from thicker, more complex textures to a more simplistic format. Classical music has a lighter, clearer texture than baroque music and is less complex. It is mainly homophonic—melody above chordal accompaniment (but counterpoint by no means is forgotten, especially later in the period). It also makes use of style galant in the classical period which was drawn in opposition to the strictures of the baroque style, emphasizing light elegance in place of the baroque’s dignified seriousness and impressive grandeur.</p>

