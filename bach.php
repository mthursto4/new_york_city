    <h1>Carl Philipp Emanuel Bach (1714-1788)</h1>

    <h2>Founding Father</h2>

    <p>The second of three sons born to the great composer, Johann Sebastian Bach, Carl Philipp Emanuel Bach (named in part to honor Georg Philipp Telemann, Bach Sr.'s friend and C.P.E. Bach's godfather), was highly revered by Mozart, Haydn, and Beethoven. C.P.E. Bach's most valuable contribution to the classical period (and the musical world as a whole) was his publication, An Essay on the True Art of Playing Keyboard Instruments. It instantly became the definitive for piano technique. To this day, it is still largely taught throughout the world.</p> 

    

