      <h1>Ludwig van Beethoven (1770-1827)</h1>

      <h2>The Renegade</h2>


        <p>Many view Beethoven as the bridge connecting the classical period to the romantic period. Beethoven only wrote nine symphonies. Compare that to Haydn and Mozart, who, combined, wrote over 150 symphonies. What makes Beethoven so special? It was Beethoven's successful attempt to break the mold of the highly structured and refined rules of classical period composition. His compositions, especially the famous Symphony No. 9, opened the flood gates of composing with emotional abandon.</p>

