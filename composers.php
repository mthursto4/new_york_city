    <h1>Wolfgang Amadeus Mozart (1756-1791)</h1>

    <h2>The Prodigy</h2>

      <p>Did you know that nearly half of Mozart's life was spent touring the European continent? Born in 1756, Mozart was a musical prodigy who began composing at the age of five. Shortly after his talent was discovered, his father was quick to take him on tours with his sister. Tragically, Mozart died at the young age of 35. Despite his short life, Mozart greatly advanced classical period music, contributing over 600 compositions. His compositional style is similar to that of Haydn's, only more flamboyant and, during his lifetime, often criticized for having "too many notes."</p> 

