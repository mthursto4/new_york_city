    <h1>Franz Joseph Haydn (1732-1809)</h1>

    <h2>Father of the Symphony</h2>

      <p>Haydn was a remarkable composer, epitomizing the meaning of classical period composition, and his music always stayed true to form. Haydn, unlike most composers, had a "reliable and steady" job composing, directing, teaching, performing, and managing musicians from the royal Esterhazy family. During this time, Haydn composed many pieces of music for the courtly orchestra to perform. With a staggering body of work, including over 100 symphonies and 60 string quartets, he is often referred to as the "Father of the Symphony" or "Father of the String Quartet."</p> 

